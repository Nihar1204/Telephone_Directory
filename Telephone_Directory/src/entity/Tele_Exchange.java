package entity;

public class Tele_Exchange extends Customer {
	private int Teid;
	private int Ecode;
	private String Addre;
	public int getTeid() {
		return Teid;
	}
	public void setTeid(int Teid) {
		this.Teid = Teid;
	}
	public int getEcode() {
		return Ecode;
	}
	public void setEcode(int Ecode) {
		this.Ecode = Ecode;
	}
	public String getAddre() {
		return Addre;
	}
	public void setAddre(String Addre) {
		this.Addre = Addre;
	}
}
