package entity;

public class Customer {
 private int Id;
 private String Name;
 private long Phno;
 private String Addr;
 private int Code;
public int getId() {
	return Id;
}
public void setId(int Id) {
	this.Id = Id;
}
public String getName() {
	return Name;
}
public void setName(String Name) {
	this.Name = Name;
}
public long getPhno() {
	return Phno;
}
public void setPhno(long Phno) {
	this.Phno = Phno;
}
public String getAddr() {
	return Addr;
}
public void setAddr(String Addr) {
	this.Addr = Addr;
}
public int getCode() {
	return Code;
}
public void setCode(int Code) {
	this.Code = Code;
}
}
